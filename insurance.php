<h2>Medicare</h2>
<p>
	Medicare pays half of their approved fee of $73.  This is called the "psychiatric reduction". 
	The rest is the responsibility of the patient. Many secondary insurance companies cover the
	remaining amount, like AARP or State Health BCBS.
</p>

<h2>Health Choice</h2>
<p>
	Health Choice usually pays the entire amount for children. Children are allowed 26 sessions
	per calendar year, and more can be requested if needed.
</p>

<h2>BCBS</h2>
<p>
	BCBS pays differing amounts dependent on your policy and your employer. If you are out of state
	BCBS you will be covered like an in state person. I am a participating provider for Federal BCBS,
	State Health, Blue Advantage, Blue Options, and Costwise.
</p>

<h2>CIGNA</h2>
<p>
	Cigna requires different co-pays based on your employer's agreement with them. Please check your individual policy. 
</p>

<h2>MHN Network</h2>
